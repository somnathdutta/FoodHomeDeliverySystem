package Bean;

public class AssignedOrderBean {
	
	public Integer orderId;
	
	public String status;
	
	public String deliveryBoy;
	
	public String deliveryBoyContactNumber;
	
	
	public Integer getOrderId() {
		return orderId;
	}
	public void setOrderId(Integer orderId) {
		this.orderId = orderId;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getDeliveryBoy() {
		return deliveryBoy;
	}
	public void setDeliveryBoy(String deliveryBoy) {
		this.deliveryBoy = deliveryBoy;
	}
	public String getDeliveryBoyContactNumber() {
		return deliveryBoyContactNumber;
	}
	public void setDeliveryBoyContactNumber(String deliveryBoyContactNumber) {
		this.deliveryBoyContactNumber = deliveryBoyContactNumber;
	}
	
}
