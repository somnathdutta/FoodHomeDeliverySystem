package Bean;

public class AboutUsBean {
	
	private Integer aboutUsId;
	private String aboutUs;
	private boolean insertDivVis;
	
	public Integer getAboutUsId() {
		return aboutUsId;
	}
	public void setAboutUsId(Integer aboutUsId) {
		this.aboutUsId = aboutUsId;
	}
	public String getAboutUs() {
		return aboutUs;
	}
	public void setAboutUs(String aboutUs) {
		this.aboutUs = aboutUs;
	}
	public boolean isInsertDivVis() {
		return insertDivVis;
	}
	public void setInsertDivVis(boolean insertDivVis) {
		this.insertDivVis = insertDivVis;
	}
	

}
