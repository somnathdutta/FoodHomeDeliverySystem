package Bean;

public class Flavour {

	private String flavourType;
	private int flavourId;
	public String getFlavourType() {
		return flavourType;
	}
	public void setFlavourType(String flavourType) {
		this.flavourType = flavourType;
	}
	public int getFlavourId() {
		return flavourId;
	}
	public void setFlavourId(int flavourId) {
		this.flavourId = flavourId;
	}
	
	
	
}
