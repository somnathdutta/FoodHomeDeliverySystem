package Bean;

public class ItemPackMaster {

	private int itemPackId;
	private String itemPackName;
	private String status;
	private String user;
	
	public int getItemPackId() {
		return itemPackId;
	}
	public void setItemPackId(int itemPackId) {
		this.itemPackId = itemPackId;
	}
	public String getItemPackName() {
		return itemPackName;
	}
	public void setItemPackName(String itemPackName) {
		this.itemPackName = itemPackName;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	
	
	
}
