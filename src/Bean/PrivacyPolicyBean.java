package Bean;

public class PrivacyPolicyBean {

	private Integer privacyPolicyId;
	private String privacyPolicy;
	private boolean insertDivVis;
	
	public Integer getPrivacyPolicyId() {
		return privacyPolicyId;
	}
	public void setPrivacyPolicyId(Integer privacyPolicyId) {
		this.privacyPolicyId = privacyPolicyId;
	}
	public String getPrivacyPolicy() {
		return privacyPolicy;
	}
	public void setPrivacyPolicy(String privacyPolicy) {
		this.privacyPolicy = privacyPolicy;
	}
	public boolean isInsertDivVis() {
		return insertDivVis;
	}
	public void setInsertDivVis(boolean insertDivVis) {
		this.insertDivVis = insertDivVis;
	}
	
}
