package Bean;

public class BikerTrack {

	private String orderNo,kitchenName,bikerName,bikerAddress,latitude,longitude,startTrip;

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getKitchenName() {
		return kitchenName;
	}

	public void setKitchenName(String kitchenName) {
		this.kitchenName = kitchenName;
	}

	public String getBikerName() {
		return bikerName;
	}

	public void setBikerName(String bikerName) {
		this.bikerName = bikerName;
	}

	public String getBikerAddress() {
		return bikerAddress;
	}

	public void setBikerAddress(String bikerAddress) {
		this.bikerAddress = bikerAddress;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	public String getStartTrip() {
		return startTrip;
	}

	public void setStartTrip(String startTrip) {
		this.startTrip = startTrip;
	}
	
}
