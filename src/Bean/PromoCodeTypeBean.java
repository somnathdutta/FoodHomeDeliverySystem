package Bean;

public class PromoCodeTypeBean {
	
	private Integer promoCodeTypeId;
	private String promocodeType;
	
	private Integer applyTypeId;
	private String applyType;
	
	public Integer getPromoCodeTypeId() {
		return promoCodeTypeId;
	}
	public void setPromoCodeTypeId(Integer promoCodeTypeId) {
		this.promoCodeTypeId = promoCodeTypeId;
	}
	public String getPromocodeType() {
		return promocodeType;
	}
	public void setPromocodeType(String promocodeType) {
		this.promocodeType = promocodeType;
	}
	public Integer getApplyTypeId() {
		return applyTypeId;
	}
	public void setApplyTypeId(Integer applyTypeId) {
		this.applyTypeId = applyTypeId;
	}
	public String getApplyType() {
		return applyType;
	}
	public void setApplyType(String applyType) {
		this.applyType = applyType;
	} 

}
