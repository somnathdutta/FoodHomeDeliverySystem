package Bean;

public class ClusterBean {

	public int kichenId,groupId;
	public String kitchenName,pincode,groupName;
	public boolean isSelected=false;
	public boolean isDeSelected=false;
	
	public int getKichenId() {
		return kichenId;
	}
	public void setKichenId(int kichenId) {
		this.kichenId = kichenId;
	}
	public String getKitchenName() {
		return kitchenName;
	}
	public void setKitchenName(String kitchenName) {
		this.kitchenName = kitchenName;
	}
	public String getPincode() {
		return pincode;
	}
	public void setPincode(String pincode) {
		this.pincode = pincode;
	}
	public boolean isSelected() {
		return isSelected;
	}
	public void setSelected(boolean isSelected) {
		this.isSelected = isSelected;
	}
	public boolean isDeSelected() {
		return isDeSelected;
	}
	public void setDeSelected(boolean isDeSelected) {
		this.isDeSelected = isDeSelected;
	}
	public int getGroupId() {
		return groupId;
	}
	public void setGroupId(int groupId) {
		this.groupId = groupId;
	}
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}
	
	
}
